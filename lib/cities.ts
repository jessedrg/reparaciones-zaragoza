export function getCityName(slug: string): string {
  return slug
    .split("-")
    .map((word) => {
      if (["de", "del", "la", "las", "los", "el", "en", "y", "a", "o"].includes(word)) return word
      return word.charAt(0).toUpperCase() + word.slice(1)
    })
    .join(" ")
}

